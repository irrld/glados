void fbterm_disable_cursor() {
  if (!cursor_enabled_) {
    return;
  }
  cursor_enabled_ = false;
}

void fbterm_enable_cursor() {
  if (cursor_enabled_) {
    return;
  }
  cursor_enabled_ = true;
}

void fbterm_clear_console() {
  cursor_pos_ = 0;
  clear_screen(background_color_);
}

void fbterm_putc(const char c) {
  if (c == 0 || c >= CHARACTER_MAX || !init_) {
    return;
  }
  if (c == '\n') {
    cursor_pos_ = (cursor_pos_ / term_width_ + 1) * term_width_;
    fbterm_handle_scroll();
    return;
  }
  uint32_t term_x = cursor_pos_ % term_width_;
  uint32_t term_y = cursor_pos_ / term_width_;

  // Left most pixel of the character on the screen
  uint32_t x = term_x * CHARACTER_SIZE;
  uint32_t y = LINE_SPACING + term_y * CHARACTER_SIZE + (term_y * LINE_SPACING);
  fbterm_draw_char(x, y, foreground_color_, c);
  cursor_pos_++;
  fbterm_handle_scroll();
}

void fbterm_draw_char(uint32_t x, uint32_t y, uint32_t color, char c) {
  // Get the character's bitmap from the font array
  unsigned char* bitmap = font[c - ASCII_OFFSET];  // ASCII offset (font starts at space ' ' = ASCII 32)

  // Iterate over each row (8 rows)
  for (int row = 0; row < CHARACTER_SIZE; row++) {
    unsigned char row_data = bitmap[row];

    // Iterate over each column (8 bits in the row)
    for (int col = 0; col < CHARACTER_SIZE; col++) {
      if (row_data & (1 << (CHARACTER_SIZE - col - 1))) {
        // Draw pixel if the corresponding bit is set
        draw_pixel(x + col, y + row, color);
      }
    }
  }
}