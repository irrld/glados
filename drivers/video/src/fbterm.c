void init_fbterm(uint32_t screen_width, uint32_t screen_height) {
  init_ = true;
  term_width_ = screen_width / CHARACTER_SIZE;
  term_height_ = screen_height / CHARACTER_SIZE;
  cursor_enabled_ = true;
  cursor_pos_ = 0;
  foreground_color_ = 0xFFFFFF; // white
  background_color_ = 0x000000; // black
  fbterm_clear_console();
  fbterm_disable_cursor();
}

